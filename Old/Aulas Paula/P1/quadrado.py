#turtle quadrado
import turtle

jade=turtle.Turtle()
jade.shape('turtle')
jade.width(10)
jade.color('blue')
jade.setheading(45)
jade.begin_fill()
jade.forward(100)
jade.left(90)
jade.forward(100)
jade.left(90)
jade.forward(100)
jade.left(90)
jade.forward(100)
jade.fillcolor('cyan')
jade.end_fill()
